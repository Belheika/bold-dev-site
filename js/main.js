/* QUOTE BUILDER */
const QB = { type: null, base: 0, feats: new Set(), timeAdd: 0 };

function qbCalc() {
  return Math.max(0, QB.base + [...QB.feats].reduce((s,f) => s + f, 0) + QB.timeAdd);
}
function qbFmt(n) { return QB.base ? '€' + n.toLocaleString() : '€—'; }
function qbUpdatePrices() {
  const p = qbCalc();
  ['qb-price','qb-price2','qb-price3'].forEach(id => {
    const el = document.getElementById(id);
    if (el) el.textContent = qbFmt(p);
  });
}

document.querySelectorAll('#qb-type-grid .qb-opt').forEach(opt => {
  opt.addEventListener('click', () => {
    document.querySelectorAll('#qb-type-grid .qb-opt').forEach(o => o.classList.remove('sel'));
    opt.classList.add('sel');
    QB.type = opt.dataset.type;
    QB.base = +opt.dataset.base;
    document.getElementById('qb-next1').disabled = false;
    qbUpdatePrices();
  });
});

document.querySelectorAll('#qb-feat-grid .qb-opt').forEach(opt => {
  opt.addEventListener('click', () => {
    const add = +opt.dataset.add;
    if (opt.classList.toggle('sel')) {
      QB.feats.add(add);
    } else {
      QB.feats.delete(add);
    }
    qbUpdatePrices();
  });
});

document.querySelectorAll('#qb-time-grid .qb-opt').forEach(opt => {
  opt.addEventListener('click', () => {
    document.querySelectorAll('#qb-time-grid .qb-opt').forEach(o => o.classList.remove('sel'));
    opt.classList.add('sel');
    QB.timeAdd = +opt.dataset.add;
    document.getElementById('qb-finish').disabled = false;
    qbUpdatePrices();
  });
});

function qbGo(step) {
  [1,2,3].forEach(n => {
    document.getElementById('qbp'+n).classList.toggle('act', n === step);
    const dot = document.getElementById('qsd'+n);
    if (dot) {
      dot.classList.toggle('act', n === step);
      dot.classList.toggle('done', n < step);
    }
  });
  if (step === 2) qbUpdatePrices();
  if (step === 3) qbUpdatePrices();
}

function qbFinish() {
  const total = qbCalc();
  document.getElementById('qb-result').style.display = 'block';
  document.getElementById('qbp3').classList.remove('act');
  const el = document.getElementById('qbr-total');
  let cur = 0; const iv = setInterval(() => {
    cur = Math.min(cur + total/50, total);
    el.textContent = '€' + Math.floor(cur).toLocaleString();
    if (cur >= total) clearInterval(iv);
  }, 18);
}

function qbReset() {
  QB.type = null; QB.base = 0; QB.feats.clear(); QB.timeAdd = 0;
  document.querySelectorAll('.qb-opt').forEach(o => o.classList.remove('sel'));
  document.getElementById('qb-next1').disabled = true;
  document.getElementById('qb-finish').disabled = true;
  document.getElementById('qb-result').style.display = 'none';
  qbGo(1);
  qbUpdatePrices();
}
