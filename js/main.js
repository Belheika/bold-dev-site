/* SCRAMBLE */
function scramble() {
  const el = document.getElementById('hero-title');
  const spans = el.querySelectorAll('span');
  const lines = ['WE BUILD SITES','THAT HIT HARD.'];
  const ch = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789@#$%&*';
  let f = 0; const tot = 48;
  const iv = setInterval(() => {
    spans.forEach((sp, li) => {
      const prog = Math.max(0, (f / tot) - li * 0.1);
      sp.textContent = lines[li].split('').map((c, i) => {
        if (i < prog * lines[li].length) return c;
        return ch[Math.floor(Math.random() * ch.length)];
      }).join('');
    });
    f++;
    if (f > tot) {
      spans.forEach((sp, li) => { sp.textContent = lines[li]; });
      clearInterval(iv);
    }
  }, 38);
}

