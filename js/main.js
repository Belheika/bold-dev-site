/* SCROLL REVEAL */
function initReveal() {
  const obs = new IntersectionObserver(ee => {
    ee.forEach(e => { if(e.isIntersecting) e.target.classList.add('vis'); });
  }, {threshold:.1, rootMargin:'0px 0px -48px 0px'});
  document.querySelectorAll('.rev').forEach(el => obs.observe(el));
}

/* COUNTER */
const cntObs = new IntersectionObserver(ee => {
  ee.forEach(e => {
    if (!e.isIntersecting) return;
    const el = e.target; const target = +el.dataset.count; let cur = 0;
    const iv = setInterval(() => {
      cur = Math.min(cur + target/60, target);
      el.textContent = Math.floor(cur);
      if (cur >= target) clearInterval(iv);
    }, 16);
    cntObs.unobserve(el);
  });
}, {threshold:.5});
document.querySelectorAll('[data-count]').forEach(el => cntObs.observe(el));

/* CYCLING */
function startCycle() {
  const el = document.getElementById('fw-cyc');
  const chips = document.querySelectorAll('.fw-chip');
  if (!el) return;
  el.style.transition = 'opacity .3s ease,transform .4s cubic-bezier(.16,1,.3,1)';
  setInterval(() => {
    cycIdx = (cycIdx + 1) % chips.length;
    el.style.opacity = '0'; el.style.transform = 'translateY(16px)';
    chips.forEach((c,i) => c.classList.toggle('active', i === cycIdx));
    setTimeout(() => {
      el.textContent = (CYCLE[lang]||CYCLE.en)[cycIdx];
      el.style.opacity = '1'; el.style.transform = 'translateY(0)';
    }, 280);
  }, 2200);
}

/* WORK HORIZONTAL SCROLL */
(function(){
  const section = document.getElementById('work');
  const track   = document.getElementById('work-track');
  const dots    = document.querySelectorAll('.wdot');
  const hint    = document.getElementById('work-hint');
  const PANELS  = 4;

  function updateWork() {
    const rect = section.getBoundingClientRect();
    const sH   = section.offsetHeight;
    const vH   = window.innerHeight;
    const prog = Math.max(0, Math.min(1, -rect.top / (sH - vH)));
    track.style.transform = `translateX(-${prog * (PANELS - 1) * 100}vw)`;
    const active = Math.min(PANELS - 1, Math.floor(prog * PANELS));
    dots.forEach((d,i) => d.classList.toggle('act', i === active));
    if (hint) hint.style.opacity = prog > 0.88 ? '0' : '1';
  }

  window.addEventListener('scroll', updateWork, { passive: true });
  updateWork();
})();

